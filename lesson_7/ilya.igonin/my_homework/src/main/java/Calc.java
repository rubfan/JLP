public class Calc {
    public long add(int a, int b) {
        return (long) a + b;
    }

    public long sub(int a, int b) {
        return (long) a - b;
    }

    public long div(int a, int b) {
        return (long) a / b;
    }

    public long mil(int a, int b) {
        return (long) a * b;
    }
}

