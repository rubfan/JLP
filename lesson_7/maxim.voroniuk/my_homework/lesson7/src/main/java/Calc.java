public class Calc {

    public static long add(int a, int b){
        return a + b;
    }

    public static long sub(int a, int b){
        return a - b;
    }

    public static long div(int a, int b){
        return a / b;
    }

    public static long mul(int a, int b){
        return a * b;
    }
}
